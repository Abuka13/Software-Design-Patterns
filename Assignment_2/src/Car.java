interface Car {
    String getName();
}

class TeslaModelS implements Car {
    public String getName() {
        return "Tesla Model S";
    }
}

class TeslaModel3 implements Car {
    public String getName() {
        return "Tesla Model 3";
    }
}

class BMWX5 implements Car {
    public String getName() {
        return "BMW X5";
    }
}

class BMWM4 implements Car {
    public String getName() {
        return "BMW M4";
    }
}
